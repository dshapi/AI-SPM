"""
platform_shared/lexical_patterns.py
────────────────────────────────────
Single source of truth for all lexical attack patterns used across the pipeline.

Two registries — kept separate because they serve different matching semantics:

PROMPT_PATTERNS
  dict[category, list[phrase]] of plain substring phrases.
  Consumed by extract_signals() (platform_shared/risk.py) for risk scoring in
  the processor and agent-orchestrator services.  Substring matching is
  intentional: ~180 precise phrases give broad coverage without regex overhead
  inside the scoring hot-path.

LEXICAL_REGEX_PATTERNS
  list[(category, raw_regex_str)] of compiled-ready regex patterns.
  Consumed by screen_lexical() (services/api/models/lexical_screen.py) for the
  API fast-block layer.  Regex matching handles natural-language variation that
  substring matching would require many separate phrases to cover.

Both registries use the same category labels so a match in either produces a
consistent signal name downstream.

Adding a new attack class
─────────────────────────
1. Add substring phrases to PROMPT_PATTERNS under the appropriate category.
2. Add a regex (or several) to LEXICAL_REGEX_PATTERNS with the same category.
3. TestLexicalParity in tests/test_risk_patterns.py will enforce that both
   registries fire on the canonical test prompts for that class.
"""
